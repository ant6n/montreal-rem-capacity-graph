def _makeSchedule(startHour, endHour, frequencyMinutes):
    result = []
    t = startHour
    endHour -= 1/60.0
    while t <= endHour:
        minute = int(round(t*60))
        result.append('%2d:%02d:00' % (minute / 60, minute % 60))
        t += frequencyMinutes/60.0
    return result

